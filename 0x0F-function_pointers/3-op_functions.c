#include "3-calc.h"
/**
* op_add - adds two numbers
* @a: first number
* @b: second number
* Return: Result of calculation
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - substracts two numbers
* @a: first number
* @b: second number
* Return: Result of calculation
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplies two numbers
* @a: first number
* @b: second number
* Return: Result of calculation
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - divides a / b
* @a: first number
* @b: second number
* Return: Result of calculation
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - returns residual of a / b
* @a: first number
* @b: second number
* Return: Result of calculation
*/
int op_mod(int a, int b)
{
	return (a % b);
}
